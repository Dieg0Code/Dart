main() {
  int x = 10 + 5; // + 15
  x = 20 - 10; // - 10
  x = 10 * 2; // * 20

  double b = 10.0 / 2; // / 5.0
  b = 10.0 % 3; // % 1  El sobrante de la division
  b = -b; // -expr  Es usado para cambiar el valor de la expresion

  int c = 10 ~/ 3; // ~/  3 Es la division entera

  double a = 10;
  a++; // a = a + 1
  a--; // a = a - 1
  a += 5; // a = a + 5
  a -= 5; // a = a - 5
  a *= 2; // a = a * 2
  a /= 2; // a = a / 2
  a %= 2; // a = a % 2
}
